# 4강. 식별자

# 식별자(identifier): 기호를 빌려서 값을 표현

# 파이썬 자체가 강제하는 규칙
#   키워드 x
#   특수문자는 _만 허용
#   숫자로 시작하면 안 된다 
#   공백을 포함할 수 없다.

# 개발자들끼리 약속한 규칙
#   최대한 알파벳을 사용하자
#   스네이크케이스(snake_case)와 캐멀케이스(CamelCase)를 사용하자!  
#     파이썬에서의 캐멀케이스는 첫문자도 대문자.     
#     캐멀케이스 : 클래스
#     스네이크케이스:  변수, 함수
#   의미 없는 단어보다는 의미 있는 단어를 사용하자!